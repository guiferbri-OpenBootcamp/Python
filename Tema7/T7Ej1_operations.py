def add(elem1: float, elem2: float) -> float:
    return float(elem1 + elem2)


def sub(elem1: float, elem2: float) -> float:
    return float(elem1 - elem2)


def mul(elem1: float, elem2: float) -> float:
    return float(elem1 * elem2)


def div(elem1: float, elem2: float) -> float:
    return float(elem1 / elem2)